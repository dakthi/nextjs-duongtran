## Hai quy tắc "không thể thương lượng"

Bà Reeves đặt ra hai nguyên tắc rõ ràng cho ngân sách:

- Không vay tiền để chi cho hoạt động thường ngày của chính phủ

- Giảm nợ công (public debt) so với thu nhập quốc gia (GDP) trước khi nhiệm kỳ này kết thúc

Nghe có vẻ hợp lý. Nhưng các chuyên gia nói, để giữ lời hứa ấy, ngân sách cần thêm 22 tỷ bảng. Và có thể là thêm 20 tỷ nữa nếu năng suất lao động không cải thiện.

Nói cách khác, để giữ đúng hai nguyên tắc này, tăng thuế gần như là điều không tránh khỏi.

## Những lựa chọn thuế có thể xuất hiện trong Ngân sách

- Thuế thu nhập và bảo hiểm quốc gia có thể tiếp tục bị đóng băng đến sau 2028.

→ Không tăng thuế suất, nhưng khi lương tăng, nhiều người sẽ phải nộp thuế cao hơn. Đây là một hình thức tăng thuế gián tiếp mà người dân ít khi để ý.

- LLPs (Limited Liability Partnerships): nhóm chuyên gia thu nhập cao có thể phải trả Employers'' NI, giúp ngân sách thu thêm khoảng 2 tỷ bảng.

- Cash ISA limit (giới hạn tiết kiệm miễn thuế) có thể bị giảm từ £20,000 xuống £10,000.

- Hóa đơn năng lượng (điện & gas): có thể giảm VAT 5% hoặc cắt bớt chi phí quản lý mà nhà cung cấp được phép chuyển sang khách hàng

- Phúc lợi xã hội: giới hạn hai con trong Universal Credit có thể được nới nhẹ, nhưng khó bị hủy bỏ hoàn toàn

- Về bất động sản, có thể sẽ là thay đổi lớn, landlords có thể sẽ phải trả nhiều thuế hơn:

• Thay Stamp Duty Land Tax bằng một loại Property Tax hàng năm

• Cải cách Council Tax

• Và trong một số trường hợp, Capital Gains Tax có thể được áp dụng ngay cả khi bán main residence (nhà ở chính)

